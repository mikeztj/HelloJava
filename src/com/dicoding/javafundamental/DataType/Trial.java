package com.dicoding.javafundamental.DataType;

public class Trial {
    public static void main(String[] args) {

        // Byte = 8 bit
        byte a = 10;
        byte b = -10;

        //Short = 16 bit
        short c = 15000;
        short d = -20000;

        //int = 32 bit
        int e = 150000;
        int f = -200000;

        //Long = 64 bit
        long g = 150000L;
        long h = -200000L;

        //Float = untuk decimal 32 bit
        float i = 3.5f;

        //double = decimal lebih besar 64 bit
        double j = 5.0;

        //boolean =  True False
        boolean k = true;
        boolean l = false;

        //String = string
        String tet= "Test";
        int length = tet.length();
        System.out.println(length);
    }
}
